#transformação de string
frase = '    Curso Em Video Python    '
print(frase.replace('Python', 'Android')) # Vai trocar a palavra Python por Android
print(frase.upper()) # Transforma o texto em maiusculo
print(frase.lower()) # Transforma em Minusculas
print(frase.capitalize()) # Transforma a 1 letra em maiusculo é o resto em minusculas
print(frase.title()) # Transforma somente a 1 letra de CADA PALAVRA em Maiusculo o resto em minusculas
print(frase.strip()) # Remove os espaços em branco e deixa só os espaços úteis
print(frase.rtrip())# Vai remover apenas os espaços do lado Direito o r e de (Right)
print(frase.ltrip()) # Vai remover apenas os espaços do lado Esquerdo o l e de (Left)


